# int
a = 10
print("a:", a, "\ttype:", type(a))

# float
b = -7.8
print("b:", b, "\ttype:", type(b))

# string
c = "salam"
print("c:", c, "\ttype:", type(c))

# bool
d = True
print("d:", d, "\ttype:", type(d))

# bool
e = False
print("e:", e, "\ttype:", type(e))

# bool
f = not d
print("f:", f, "\ttype:", type(f))


